def match_scene_answer(scene: str, question: str) -> str:
    """根据场景和问题关键词匹配最合适的降级回答"""
    scene_fallbacks = SCENE_FALLBACKS.get(scene, {})
    if not scene_fallbacks:
        return "抱歉，我暂时无法回答这个问题，请稍后再试。"

    # 完全匹配：所有关键词都在问题中
    for keywords, answer in scene_fallbacks.items():
        if keywords == "default":
            continue
        kw_list = keywords.split("|")
        if all(kw in question for kw in kw_list):
            return answer

    # 部分匹配：任意关键词命中
    for keywords, answer in scene_fallbacks.items():
        if keywords == "default":
            continue
        kw_list = keywords.split("|")
        if any(kw in question for kw in kw_list):
            return answer

    # 默认回答
    return scene_fallbacks.get("default", "抱歉，我暂时无法回答这个问题。")
